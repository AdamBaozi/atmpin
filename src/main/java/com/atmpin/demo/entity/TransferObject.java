package com.atmpin.demo.entity;

public class TransferObject {
    private String fekName;
    private String fek;
    private String eiName;

    public String getFekName() {
        return fekName;
    }

    public void setFekName(String fekName) {
        this.fekName = fekName;
    }

    public String getFek() {
        return fek;
    }

    public void setFek(String fek) {
        this.fek = fek;
    }

    public String getEiName() {
        return eiName;
    }

    public void setEiName(String eiName) {
        this.eiName = eiName;
    }
}
